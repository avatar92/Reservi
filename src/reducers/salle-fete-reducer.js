const salleFeteReducer = (state = allSalle, action) => {
    switch (action.type) {
        case ('SEARCH_SALLE_FETE'):
            return allSalle.filter(el => {
                if(action.searchTerm.trim() === ''){
                    return true
                }else{
                    return (el.salleName.toLocaleLowerCase().indexOf(action.searchTerm) > -1 || el.location.toLocaleLowerCase().indexOf(action.searchTerm) > -1)
                }
            });
        case "REMOVE_SALLE_FETE": 
            return state.filter(el=>el._id!==action._id)
        default:
            return state;
    }
}
export default salleFeteReducer